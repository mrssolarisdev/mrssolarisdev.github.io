ORG 0000h
	JMP MAIN
ORG 001Bh
	CALL TIMEOUT
ORG 0033h
TIMEOUT:	
	MOV TH0,#0B1h
	MOV TL0,#0DFh
	MOV A,P1
	CPL A
	MOV P1,A
RETI
MAIN:
	MOV PSW, #00h
	MOV SP,#60h
	;CONTROLE INTS
	MOV IE, #10001000b
	MOV IP, #0000100b
	MOV TCON,#00001000b
	MOV TMOD, #00100000b
	
	;CONFIG TIMERS
	MOV TH1,#0CDH
	MOV TL1, #0CDh
	MOV P1,#10110110b
	JMP $

END